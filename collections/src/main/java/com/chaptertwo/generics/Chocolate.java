package com.chaptertwo.generics;

public class Chocolate {
    private final String name;
    private final int size;

    public Chocolate(final String name, final int size) {
        this.name = name;
        this.size = size;
    }

    public String getName() {
        return name;
    }

    public int getSize() {
        return size;
    }
}
