package com.chaptertwo.generics;

public class Waffle {
    private final String name;
    private final int quantity;

    public Waffle(final String name, final int quantity) {
        this.name = name;
        this.quantity = quantity;
    }

    public String getName() {
        return name;
    }

    public int getQuantity() {
        return quantity;
    }
}
